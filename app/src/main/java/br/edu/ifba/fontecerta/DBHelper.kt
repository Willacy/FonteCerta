    }

    private fun inserirPeca(db: SQLiteDatabase?, nome: String, consumo: Int, categoria: String) {
        val insertQuery = """
            INSERT INTO $TABLE_NAME ($COLUMN_NOME, $COLUMN_CONSUMO, $COLUMN_CATEGORIA)
            VALUES ('$nome', $consumo, '$categoria');
        """
        db?.execSQL(insertQuery)
    }
